BASE_DIR = r"C:\Users\ehrbs\projects\dok6n\조각난_마도서"
FONT_NAME = "맑은 고딕"
BODY_SIZE = Pt(11)
H1_SIZE = Pt(16)
H2_SIZE = Pt(13)
H3_SIZE = Pt(12)


def set_run_font(run, name=FONT_NAME):
    run.font.name = name
    rpr = run._element.get_or_add_rPr()
    rfonts = rpr.find(qn('w:rFonts'))
    if rfonts is None:
        rfonts = OxmlElement('w:rFonts')
        rpr.append(rfonts)
    rfonts.set(qn('w:eastAsia'), name)
    rfonts.set(qn('w:ascii'), name)
    rfonts.set(qn('w:hAnsi'), name)


def configure_doc(doc):
    style = doc.styles['Normal']
    style.font.name = FONT_NAME
    style.font.size = BODY_SIZE
    rpr = style.element.get_or_add_rPr()
    rfonts = rpr.find(qn('w:rFonts'))
    if rfonts is None:
        rfonts = OxmlElement('w:rFonts')
        rpr.append(rfonts)
    rfonts.set(qn('w:eastAsia'), FONT_NAME)
    rfonts.set(qn('w:ascii'), FONT_NAME)
    rfonts.set(qn('w:hAnsi'), FONT_NAME)
    pf = style.paragraph_format
    pf.line_spacing = 1.6
    pf.space_after = Pt(4)


def add_heading(doc, text, level=1):
    sizes = {1: H1_SIZE, 2: H2_SIZE, 3: H3_SIZE}
    p = doc.add_paragraph()
    if level == 1:
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        p.paragraph_format.space_before = Pt(24)
        p.paragraph_format.space_after = Pt(16)
    elif level == 2:
        p.paragraph_format.space_before = Pt(16)
        p.paragraph_format.space_after = Pt(8)
    else:
        p.paragraph_format.space_before = Pt(10)
        p.paragraph_format.space_after = Pt(4)
    r = p.add_run(text)
    r.bold = True
    r.font.size = sizes[level]
    set_run_font(r)
    return p


def parse_inline(text):
    pat = re.compile(r'(\*\*\*.+?\*\*\*|\*\*.+?\*\*|\*.+?\*)')
    parts = pat.split(text)
    out = []
    for part in parts:
        if not part:
            continue
        if part.startswith('***') and part.endswith('***'):
            out.append((part[3:-3], True, True))
        elif part.startswith('**') and part.endswith('**'):
            out.append((part[2:-2], True, False))
        elif part.startswith('*') and part.endswith('*'):
            out.append((part[1:-1], False, True))
        else:
            out.append((part, False, False))
    return out


def add_rich(doc, text, indent=None, italic_all=False, center=False, quote=False):
    p = doc.add_paragraph()
    if center:
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    if indent is not None:
        p.paragraph_format.left_indent = Cm(indent)
    if quote:
        p.paragraph_format.left_indent = Cm(1.2)
        p.paragraph_format.right_indent = Cm(1.2)
        p.paragraph_format.space_before = Pt(6)
        p.paragraph_format.space_after = Pt(6)
    for t, b, i in parse_inline(text):
        r = p.add_run(t)
        r.bold = b
        r.italic = i or italic_all
        set_run_font(r)
    return p


def add_divider(doc):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.space_before = Pt(8)
    p.paragraph_format.space_after = Pt(8)
    r = p.add_run('─' * 24)
    r.font.color.rgb = RGBColor(0x88, 0x88, 0x88)
    set_run_font(r)


def add_table(doc, rows):
    cols = len(rows[0])
    t = doc.add_table(rows=len(rows), cols=cols)
    t.style = 'Light Grid Accent 1'
    for ri, row in enumerate(rows):
        for ci, txt in enumerate(row):
            cell = t.rows[ri].cells[ci]
            cell.text = ''
            p = cell.paragraphs[0]
            for seg, b, i in parse_inline(txt):
                r = p.add_run(seg)
                r.bold = b or (ri == 0)
                r.italic = i
                set_run_font(r)


def render(doc, content):
    lines = content.split('\n')
    i = 0
    while i < len(lines):
        line = lines[i]
        s = line.rstrip()
        stripped = s.strip()
        if not stripped:
            i += 1
            continue
        if stripped.startswith('### '):
            add_heading(doc, stripped[4:], 3)
        elif stripped.startswith('## '):
            add_heading(doc, stripped[3:], 2)
        elif stripped.startswith('# '):
            add_heading(doc, stripped[2:], 1)
        elif stripped == '---':
            pass
        elif stripped.startswith('>> '):
            add_rich(doc, stripped[3:], quote=True, italic_all=True)
        elif stripped.startswith('> '):
            add_rich(doc, stripped[2:], quote=True)
        elif stripped.startswith('|') and stripped.endswith('|'):
            rows = []
            while i < len(lines) and lines[i].strip().startswith('|') and lines[i].strip().endswith('|'):
                cells = [c.strip() for c in lines[i].strip().strip('|').split('|')]
                rows.append(cells)
                i += 1
            rows = [r for r in rows if not all(set(c) <= set('-: ') and c for c in r)]
            if rows:
                add_table(doc, rows)
            continue
        elif stripped.startswith('- ') or stripped.startswith('• '):
            body = stripped[2:]
            p = doc.add_paragraph(style='List Bullet')
            for t, b, it in parse_inline(body):
                r = p.add_run(t)
                r.bold = b
                r.italic = it
                set_run_font(r)
        else:
            add_rich(doc, stripped)
        i += 1


def build_doc(relpath, title, content):
    doc = Document()
    configure_doc(doc)
    add_heading(doc, title, 1)
    render(doc, content)
    path = os.path.join(BASE_DIR, relpath)
    os.makedirs(os.path.dirname(path), exist_ok=True)
    doc.save(path)
    print(f'[OK] {path}')


